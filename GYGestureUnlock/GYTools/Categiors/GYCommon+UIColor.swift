import UIKit


extension UIColor {
    
    class func colorWithRgba(r: CGFloat, g: CGFloat, b: CGFloat, a:CGFloat)-> UIColor {
        
        
        return UIColor(red: r, green: g, blue: b, alpha: a)
    
    }
    
}
